fnum = input("Enter a num :")
snum = input("Enter another :")

if (fnum == snum):
	print("{0} == {1}".format(fnum,snum))
if (fnum != snum):
	print("{0} != {1}".format(fnum,snum))
if (fnum < snum):
        print("{0} < {1}".format(fnum,snum))
if (fnum > snum):
        print("{0} > {1}".format(fnum,snum))
if (fnum <= snum):
        print("{0} <= {1}".format(fnum,snum))
if (fnum >= snum):
        print("{0} >= {1}".format(fnum,snum))



